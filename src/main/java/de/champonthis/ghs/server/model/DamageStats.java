package de.champonthis.ghs.server.model;

public class DamageStats {

    private Integer dealtDamage;
    private Integer monsterDamage;
    private Integer otherDamage;
    private Integer healedDamage;
    private Integer heals;
    private Integer normalKills;
    private Integer eliteKills;
    private Integer bossKills;
    private Integer exhausts;
    private Integer maxDealtDamage;
    private Integer maxDamage;

    public Integer getDealtDamage() {
        return dealtDamage;
    }

    public void setDealtDamage(Integer dealtDamage) {
        this.dealtDamage = dealtDamage;
    }

    public Integer getMonsterDamage() {
        return monsterDamage;
    }

    public void setMonsterDamage(Integer monsterDamage) {
        this.monsterDamage = monsterDamage;
    }

    public Integer getOtherDamage() {
        return otherDamage;
    }

    public void setOtherDamage(Integer otherDamage) {
        this.otherDamage = otherDamage;
    }

    public Integer getHealedDamage() {
        return healedDamage;
    }

    public void setHealedDamage(Integer healedDamage) {
        this.healedDamage = healedDamage;
    }

    public Integer getHeals() {
        return heals;
    }

    public void setHeals(Integer heals) {
        this.heals = heals;
    }

    public Integer getNormalKills() {
        return normalKills;
    }

    public void setNormalKills(Integer normalKills) {
        this.normalKills = normalKills;
    }

    public Integer getEliteKills() {
        return eliteKills;
    }

    public void setEliteKills(Integer eliteKills) {
        this.eliteKills = eliteKills;
    }

    public Integer getBossKills() {
        return bossKills;
    }

    public void setBossKills(Integer bossKills) {
        this.bossKills = bossKills;
    }

    public Integer getExhausts() {
        return exhausts;
    }

    public void setExhausts(Integer exhausts) {
        this.exhausts = exhausts;
    }

    public Integer getMaxDealtDamage() {
        return maxDealtDamage;
    }

    public void setMaxDealtDamage(Integer maxDealtDamage) {
        this.maxDealtDamage = maxDealtDamage;
    }

    public Integer getMaxDamage() {
        return maxDamage;
    }

    public void setMaxDamage(Integer maxDamage) {
        this.maxDamage = maxDamage;
    }

}
